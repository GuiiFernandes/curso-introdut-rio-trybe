let roupa= 'camiseta'; //o valor 'camiseta' foi atribuído à variável de nome roupa.
//Declarar uma variável é o mesmo que dizer “criar uma variável”. Em programação, declarar significa dizer ao computador para reservar um espaço na memória para armazenar um dado.
console.log(roupa);
//As variáveis guardam somente um único valor e, toda vez que a variável receber um novo valor, o antigo é substituído.
roupa= 'moletom'; //a variável roupa passou a ter um novo valor: 'moletom'.
console.log(roupa);

let comidaFavorita= 'Hambúrguer';
console.log(comidaFavorita);
comidaFavorita= 'Lasanha';
console.log(comidaFavorita);

//Com a palavra-chave let você poderá reatribuir valores à sua variável quantas vezes quiser. Com a palavra-chave const você só poderá atribuir o valor uma única vez.
const meuNome= 'Gui';
console.log(meuNome);